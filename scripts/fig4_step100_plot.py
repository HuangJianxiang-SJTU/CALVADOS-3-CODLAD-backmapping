    ax.set_ylabel('Helix fraction (%)')
    ax.set_title('p15PAF (PED00016) residues 54–60', fontsize=14, fontweight='bold')
    ax.set_ylim(0, 22)
    ax.set_xticks(range(54, 61))
    ax.legend(fontsize=12, loc='upper left')
    style_ax(ax)

    # D: Step=5 vs step=100 mean helix bar chart
    ax = axes[1, 1]
    systems = df_summary['system'].values
    step5_vals = df_summary['step5_pred_mean_helix_pct'].values
    step100_vals = df_summary['pred_mean_helix_pct'].values
    x_pos = np.arange(len(systems))
    w = 0.35
    ax.bar(x_pos - w/2, step5_vals, w, color=C_STEP5, alpha=0.9,
           label='Step = 5', edgecolor='white', lw=0.3)
    ax.bar(x_pos + w/2, step100_vals, w, color=C_STEP100, alpha=0.9,
           label='Step = 100', edgecolor='white', lw=0.3)
    ax.set_xticks(x_pos)
    ax.set_xticklabels(systems, rotation=0, ha='center', fontsize=10)
    ax.set_ylabel('Mean predicted helix (%)')
    ax.legend(fontsize=12, loc='upper right')
    style_ax(ax)

    fig.tight_layout()
    fig.savefig(os.path.join(OUT, 'figure_s4.png'), bbox_inches='tight')
    fig.savefig(os.path.join(OUT, 'figure_s4.pdf'), bbox_inches='tight')
    plt.close(fig)
    print('  figure_s4.png / figure_s4.pdf saved')


